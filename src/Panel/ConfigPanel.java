    private void jList_annee_scolaireMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList_annee_scolaireMouseReleased
        int index = jList_annee_scolaire.getSelectedIndex();
        menu_item_remove_annee.setEnabled(true);
        menu_item_update_annee.setEnabled(true);
        menu_item_select_annee.setEnabled(true);
        if(index < 0){
            menu_item_remove_annee.setEnabled(false);
            menu_item_update_annee.setEnabled(false);
            menu_item_select_annee.setEnabled(false);
        }
        if(evt.isPopupTrigger() && evt.getComponent() instanceof JList){
            popup_menu_list_annee.show(evt.getComponent(), evt.getX(), evt.getY());
            popup_menu_list_annee.setVisible(true);
        }
    }//GEN-LAST:event_jList_annee_scolaireMouseReleased

